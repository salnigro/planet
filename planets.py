class planet(object):


    def __init__(self,obp,name,grav,mass):
        self.name = name
        self.obp = obp
        self.grav = grav
        self.mass = mass

    def getobp():
        return obp
    def getname():
        return name
    def getgrav():
        return grav
    def getmass():
        return mass
    
